#include <iostream>
using namespace std;

int main(){

    int i,j,n;
//==================================================================
    i=0;
    n = i++;
    /*
    n=0 parce que on stock la valeur de i dans n avant d'acrementer
    i++ => i = i + 1 => i = 11
    */
    cout << "A:i= " << i << " n = "<< n << "\n";
    //A:i = 1 n = 0

//==================================================================
    i = 10;
    n = ++i;
    cout <<"B:i= "<< i <<" n = "<< n <<"\n";
    /*B:i= 11 n = 11

    n = ++i ca veut dire qu'on acrement i par 1 puis on le stock dans n
    i=10 ++i => i = 11 
    */

//==================================================================
    i = 20;
    j =5;
    n = i++ * ++ j; // n = 20 * 6 = 120 ; i = i+1 = 21; j = j + 1 =6
    cout<< "C:i = "<< i << " j = "<< j << " n= "<< n <<"\n"; 
    //C:i = 21 j = 6 n= 120

//==================================================================
    i = 15;
    n = i+=3; //n = i = i+3; n = 18; i = 18
    cout<<"D:i = "<< i <<" n = "<< n <<"\n";
    //D:i = 18 n = 18


//==================================================================
    i = 3;
    j = 5;
    n = i *= --j;
    cout<<"E:i = "<< i <<" j = "<< j <<" n = "<< n <<"\n";
    // E:i = 12 j = 4 n = 12
    
    
    
    return 0;
}

